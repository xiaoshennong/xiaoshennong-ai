documents = []
for book, section, text in classics:
    full_text = f'《{book}》·{section}\n{text}'
    doc_id = f'classic_{book}_{section}'
    documents.append({
        'text': full_text,
        'metadata': {
            'source_book': f'《{book}》',
            'source_section': section,
            'original_text': text,
            'chunk_id': doc_id,
            'type': 'classic'
        }
    })

print(f'Generated {len(documents)} classic records')

rag = XiaoShennongRAG(db_path='data/chroma_db_v2')
before = rag._get_collection().count()
print(f'Before: {before}')

for i in range(0, len(documents), 50):
    batch = documents[i:i+50]
    rag.add_documents(batch)

after = rag._get_collection().count()
print(f'After: {after}')
print(f'Added: {after - before}')
